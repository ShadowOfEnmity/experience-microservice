package by.kostrikov.industry.dto;

public class IndustryDto {
    private Long id;
    private String name;

    public IndustryDto() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
